// while loops

let index = 0;
while (index <= 10) {
    // console.log(`value of index is ${index}`);
    index = index + 2;
}

let cars = ["BMW", "Lamborghini", "Koenigsegg", "Pagani", "Ferrari"];
let arr = 0;
while (arr < cars.length) {
    // console.log(`Value is ${cars[arr]}`);
    arr = arr + 1;
}

// do while loop

let score = 11;
do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);